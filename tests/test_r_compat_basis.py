"""R compatibility tests for pydlnm basis functions.

Each test loads a pre-computed fixture generated by R's dlnm package and
asserts element-wise equality (within floating-point tolerance) against the
PyDLNM implementation.

Fixtures are generated by running:
    Rscript tests/fixtures/generate_fixtures.R

Tests are automatically skipped when fixture files are absent, so CI passes
even before fixtures have been committed.
"""

import numpy as np
import pytest

import pydlnm
from r_compat_helpers import FIXTURES_DIR, load_fixture_matrix, load_fixture_vector, require_fixtures

# ---------------------------------------------------------------------------
# Tolerance — match R to ~6 significant figures (double precision round-trip)
# ---------------------------------------------------------------------------
RTOL = 1e-5
ATOL = 1e-9


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _check(py_mat: np.ndarray, r_csv: str) -> None:
    """Compare a Python basis matrix against the R golden fixture."""
    r_mat = (
        load_fixture_vector(r_csv).reshape(-1, 1)
        if py_mat.ndim == 1
        else load_fixture_matrix(r_csv)
    )
    np.testing.assert_allclose(
        py_mat,
        r_mat,
        rtol=RTOL,
        atol=ATOL,
        err_msg=f"Mismatch vs R fixture '{r_csv}'",
    )


# ---------------------------------------------------------------------------
# Natural splines — ns
# ---------------------------------------------------------------------------


class TestNsBasis:
    @require_fixtures("synthetic_x.csv", "onebasis_ns_df4.csv")
    def test_ns_df4(self, synthetic_x):
        ob = pydlnm.onebasis(synthetic_x, fun="ns", df=4)
        _check(np.asarray(ob), "onebasis_ns_df4.csv")

    @require_fixtures("synthetic_x.csv", "onebasis_ns_df4_intercept.csv")
    def test_ns_df4_intercept(self, synthetic_x):
        ob = pydlnm.onebasis(synthetic_x, fun="ns", df=4, intercept=True)
        _check(np.asarray(ob), "onebasis_ns_df4_intercept.csv")

    @require_fixtures("synthetic_x.csv", "onebasis_ns_knots.csv")
    def test_ns_explicit_knots(self, synthetic_x):
        ob = pydlnm.onebasis(synthetic_x, fun="ns", knots=np.array([15.0, 20.0, 25.0]))
        _check(np.asarray(ob), "onebasis_ns_knots.csv")

    def test_ns_shape(self, synthetic_x):
        """Shape invariant: df columns regardless of fixture availability."""
        ob = pydlnm.onebasis(synthetic_x, fun="ns", df=4)
        assert ob.shape == (len(synthetic_x), 4)

    def test_ns_shape_intercept(self, synthetic_x):
        # ns(df=4, intercept=True): df counts include the intercept, so still 4 cols
        ob = pydlnm.onebasis(synthetic_x, fun="ns", df=4, intercept=True)
        assert ob.shape == (len(synthetic_x), 4)


