# ---------------------------------------------------------------------------
# Strata
# ---------------------------------------------------------------------------


class TestStrataBasis:
    @require_fixtures("synthetic_x.csv", "onebasis_strata_df3.csv")
    def test_strata_df3(self, synthetic_x):
        ob = pydlnm.onebasis(synthetic_x, fun="strata", df=3)
        _check(np.asarray(ob), "onebasis_strata_df3.csv")

    def test_strata_is_indicator(self, synthetic_x):
        """All entries must be 0 or 1; reference-category rows sum to 0, others to 1."""
        ob = pydlnm.onebasis(synthetic_x, fun="strata", df=3)
        mat = np.asarray(ob)
        assert np.all((mat == 0) | (mat == 1))
        row_sums = mat.sum(axis=1)
        assert np.all((row_sums == 0) | (row_sums == 1))


# ---------------------------------------------------------------------------
# Integer
# ---------------------------------------------------------------------------


class TestIntegerBasis:
    @require_fixtures("onebasis_integer_lagseq.csv")
    def test_integer_lagseq(self):
        lag_seq = np.arange(0, 11, dtype=float)
        ob = pydlnm.onebasis(lag_seq, fun="integer")
        r_mat = load_fixture_matrix("onebasis_integer_lagseq.csv")
        np.testing.assert_allclose(np.asarray(ob), r_mat, rtol=RTOL, atol=ATOL)

    def test_integer_is_indicator(self):
        lag_seq = np.arange(0, 5, dtype=float)
        ob = pydlnm.onebasis(lag_seq, fun="integer")
        mat = np.asarray(ob)
        assert np.all((mat == 0) | (mat == 1))
